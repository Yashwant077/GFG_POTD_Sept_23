class Solution {
   public:
   int n,m;
   void dfs(int i,int j,vector<vector<int>> &grid){
      grid[i][j]=2;
      for(int k=0;k<4;k++){
          int ni=i+dir[k][0];
          int nj=j+dir[k][1];
          if(ni>=0 and ni<n and nj>=0 and nj<m and grid[ni][nj]==1){
              dfs(ni,nj,grid);
          }
      }
   }
   vector<vector<int>>dir={{-1,0},{0,1},{1,0},{0,-1}};
    int numberOfEnclaves(vector<vector<int>> &grid) {
        // Code here
         n=grid.size(),m=grid[0].size();
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                if((i==0 or i==n-1 or j==0 or j==m-1) and grid[i][j]==1){
                    dfs(i,j,grid);
                }
            }
        }
        int cnt=0;
        for(int i=0;i<n;i++){
            for(int j=0;j<m;j++){
                if(grid[i][j]==1){
                    cnt++;
                }
            }
        }
        return cnt;
    }
};


